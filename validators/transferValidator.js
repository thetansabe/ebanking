const { check } = require('express-validator');


module.exports = [
    check('phonenumber')
    .exists().withMessage('Vui lòng cung cấp số điện thoại của người nhận')
    .notEmpty().withMessage('Không được để trống số điện thoại của người nhận'),

    check('money')
    .exists().withMessage('Vui lòng cung cấp số tiền cần chuyển')
    .notEmpty().withMessage('Số tiền cần chuyển không được để trống')
    .isNumeric().withMessage('Số tiền cần chuyển phải là kiểu số'),

    check('message')
    .exists().withMessage('Vui lòng cung cấp ghi chú'),

]
